export const getAppointmentsByPatient = (patientId: string): Appointment[] => {
  return appointments.filter((a) => a.pazienteId === patientId);
};

export const getAppointmentsByDate = (date: string): Appointment[] => {
  return appointments.filter((a) => a.data === date);
};

export const getAppointmentsByStatus = (status: Appointment['stato']): Appointment[] => {
  return appointments.filter((a) => a.stato === status);
};

export const getUpcomingAppointments = (): Appointment[] => {
  const today = new Date().toISOString().split('T')[0];
  return appointments.filter((a) => a.data >= today && a.stato === 'confermato');
};

export const getAppointmentsByTherapist = (therapist: string): Appointment[] => {
  return appointments.filter((a) => a.terapeuta === therapist);
};
